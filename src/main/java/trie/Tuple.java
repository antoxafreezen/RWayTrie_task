package trie;

/**
 * Class <code>Tuple</code> implements pair word and its length.
 */
public class Tuple {

    String word;

    int length;

    /**
     * Constructor with word and length parameter.
     * @param word
     * @param length
     */
    public Tuple(String word, int length) {
        this.word = word;
        this.length = length;
    }
}
